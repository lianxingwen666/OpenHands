def has_chinese_comment(file_path):
    """检查文件是否已有中文注释"""
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
            # 检查是否包含中文注释标识
            return 'OpenHands' in content and ('模块' in content or '工具' in content)
    except Exception:
        return False


def add_comment_to_file(file_path, comment_template):
    """为文件添加注释"""
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()

        # 在文件开头添加注释
        new_content = comment_template + '\n\n' + content

        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(new_content)

        return True
    except Exception as e:
        print(f'处理文件 {file_path} 时出错: {e}')
        return False


def main():
    """主函数"""
    print('开始为OpenHands Utils文件添加注释...')

    utils_dir = Path('/workspace/OpenHands/openhands/utils')

    # 获取所有Python文件
    python_files = list(utils_dir.glob('*.py'))
    print(f'找到 {len(python_files)} 个Python文件')

    processed_count = 0
    skipped_count = 0

    for file_path in python_files:
        file_name = file_path.name

        # 跳过已处理的文件
        if has_chinese_comment(file_path):
            print(f'文件 {file_path} 已有注释，跳过')
            skipped_count += 1
            continue

        # 获取对应的注释模板
        if file_name in COMMENTS_TEMPLATES:
            comment_template = COMMENTS_TEMPLATES[file_name]
            if add_comment_to_file(file_path, comment_template):
                print(f'已为文件 {file_path} 添加注释')
                processed_count += 1
            else:
                print(f'处理文件 {file_path} 失败')
        else:
            print(f'未找到文件 {file_name} 的注释模板')

    print('\n注释添加完成！')
    print(f'处理文件数: {processed_count}')
    print(f'跳过文件数: {skipped_count}')
    print(f'总文件数: {len(python_files)}')


if __name__ == '__main__':
    main()
